/**
 * Per-token pricing (USD) for Bedrock foundation models.
 *
 * Prices as of March 2026 for EU cross-region inference profiles.
 * Input/output costs are per 1,000 tokens.
 *
 * @see https://aws.amazon.com/bedrock/pricing/
 */
const MODEL_PRICING: Record<string, { inputPer1k: number; outputPer1k: number; thinkingPer1k: number }> = {
    'eu.anthropic.claude-sonnet-4-6-20260310-v1:0': {
        inputPer1k: 0.003,
        outputPer1k: 0.015,
        thinkingPer1k: 0.015,
    },
    'eu.anthropic.claude-haiku-3-5-20241022-v1:0': {
        inputPer1k: 0.0008,
        outputPer1k: 0.004,
        thinkingPer1k: 0.004,
    },
};

/** Fallback pricing if the model is not in the pricing table. */
const DEFAULT_PRICING = { inputPer1k: 0.003, outputPer1k: 0.015, thinkingPer1k: 0.015 };

/**
 * Estimate the USD cost of a single Bedrock invocation.
 *
 * @param model - Foundation model identifier
 * @param usage - Token usage breakdown
 * @returns Estimated cost in USD (rounded to 6 decimal places)
 */
export function estimateInvocationCost(model: string, usage: TokenUsage): number {
    const pricing = MODEL_PRICING[model] ?? DEFAULT_PRICING;
    const cost =
        (usage.inputTokens / 1000) * pricing.inputPer1k +
        (usage.outputTokens / 1000) * pricing.outputPer1k +
        (usage.thinkingTokens / 1000) * pricing.thinkingPer1k;
    return Math.round(cost * 1_000_000) / 1_000_000;
}

