/**
 * Emit CloudWatch custom metrics via EMF for a successful pipeline execution.
 *
 * Prints a structured JSON object to stdout that CloudWatch automatically
 * parses into metrics. Zero-latency, zero-SDK-overhead approach.
 *
 * Namespace: `BedrockPublisher`
 * Dimensions: `[Environment, Model]` for cost/performance slicing
 *
 * @param ctx - Complete pipeline execution context
 */
export function emitPipelineMetrics(ctx: PipelineMetricsContext): void {
    const estimatedCost = estimateInvocationCost(ctx.model, ctx.tokenUsage);

    /** Short model label for Grafana display (strip region prefix and version suffix) */
    const modelLabel = ctx.model
        .replace(/^eu\./, '')
        .replace(/anthropic\./, '')
        .replace(/-\d{8}-v\d+:\d+$/, '');

    const emfPayload: Record<string, unknown> = {
        _aws: {
            Timestamp: Date.now(),
            CloudWatchMetrics: [
                {
                    Namespace: 'BedrockPublisher',
                    Dimensions: [['Environment', 'Model']],
                    Metrics: [
                        { Name: 'ArticlesPublished', Unit: 'Count' },
                        { Name: 'InputTokens', Unit: 'Count' },
                        { Name: 'OutputTokens', Unit: 'Count' },
                        { Name: 'ThinkingTokens', Unit: 'Count' },
                        { Name: 'EstimatedCostUSD', Unit: 'None' },
                        { Name: 'ProcessingDurationMs', Unit: 'Milliseconds' },
                        { Name: 'DraftSizeBytes', Unit: 'Bytes' },
                        { Name: 'TechnicalConfidence', Unit: 'None' },
                    ],
                },
                {
                    Namespace: 'BedrockPublisher',
                    Dimensions: [['Environment', 'ComplexityTier']],
                    Metrics: [
                        { Name: 'ContentComplexity', Unit: 'Count' },
                    ],
                },
            ],
        },

        // Dimension values
        Environment: ctx.environment,
        Model: modelLabel,
        ComplexityTier: ctx.complexityTier,

        // Metric values
        ArticlesPublished: 1,
        InputTokens: ctx.tokenUsage.inputTokens,
        OutputTokens: ctx.tokenUsage.outputTokens,
        ThinkingTokens: ctx.tokenUsage.thinkingTokens,
        EstimatedCostUSD: estimatedCost,
        ProcessingDurationMs: ctx.stageDurations.totalMs,
        DraftSizeBytes: ctx.draftSizeBytes,
        TechnicalConfidence: ctx.technicalConfidence,
        ContentComplexity: 1,

        // High-cardinality fields (searchable in logs, NOT dimensions)
        Slug: ctx.slug,
        Mode: ctx.mode,
        BedrockConverseMs: ctx.stageDurations.bedrockConverseMs ?? 0,
        S3WriteMs: ctx.stageDurations.s3WriteMs ?? 0,
        DynamoWriteMs: ctx.stageDurations.dynamoWriteMs ?? 0,
    };

    // Append QA metrics if QA agent was invoked
    if (ctx.qaScore !== undefined) {
        emfPayload.QaScore = ctx.qaScore;
        emfPayload.QaDurationMs = ctx.qaDurationMs ?? 0;
    }

    console.log(JSON.stringify(emfPayload));
}

/**
 * Emit CloudWatch custom metrics for a pipeline failure.
 *
 * Tracks which stage failed and how long the pipeline ran before failure.
 *
 * @param ctx - Failure context with stage and error details
 */
export function emitFailureMetrics(ctx: FailureMetricsContext): void {
    /** Short model label for consistency */
    const modelLabel = ctx.model
        .replace(/^eu\./, '')
        .replace(/anthropic\./, '')
        .replace(/-\d{8}-v\d+:\d+$/, '');

    const emfPayload = {
        _aws: {
            Timestamp: Date.now(),
            CloudWatchMetrics: [
                {
                    Namespace: 'BedrockPublisher',
                    Dimensions: [['Environment', 'FailureStage']],
                    Metrics: [
                        { Name: 'ArticlesFailed', Unit: 'Count' },
                        { Name: 'FailureDurationMs', Unit: 'Milliseconds' },
                    ],
                },
            ],
        },

        // Dimension values
        Environment: ctx.environment,
        Model: modelLabel,
        FailureStage: ctx.failureStage,

        // Metric values
        ArticlesFailed: 1,
        FailureDurationMs: ctx.elapsedMs,

        // High-cardinality fields (logs only)
        Slug: ctx.slug ?? 'unknown',
        ErrorMessage: ctx.errorMessage.substring(0, 500),
    };

    console.log(JSON.stringify(emfPayload));
}
