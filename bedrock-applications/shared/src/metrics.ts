/**
 * @format
 * EMF (Embedded Metric Format) Custom Metrics Module
 *
 * Provides typed, structured CloudWatch metric emission for the
 * Bedrock content pipeline. Uses EMF format — CloudWatch automatically
 * extracts metrics from structured JSON logs without any SDK calls.
 *
 * Namespace: `BedrockPublisher`
 *
 * @see https://docs.aws.amazon.com/AmazonCloudWatch/latest/monitoring/CloudWatch_Embedded_Metric_Format.html
 */

// =============================================================================
// TYPES
// =============================================================================

/**
 * Complexity tier classification (mirrors index.ts ComplexityTier).
 * Re-declared here to avoid circular dependency.
 */
type ComplexityTier = 'LOW' | 'MID' | 'HIGH';

/**
 * Pipeline stage identifiers for failure tracking.
 * Each stage maps to a distinct phase of the content pipeline.
 */
export type FailureStage =
    | 'read-draft'
    | 'kb-retrieval'
    | 'complexity-analysis'
    | 'bedrock-converse'
    | 'parse-response'
    | 'validate-content'
    | 'qa-validation'
    | 's3-write'
    | 'dynamodb-write'
    | 'isr-revalidation';

/**
 * Token usage breakdown from a Bedrock Converse API response.
 */
export interface TokenUsage {
    /** Tokens sent to the model (prompt + system) */
    readonly inputTokens: number;
    /** Tokens generated by the model (response body) */
    readonly outputTokens: number;
    /** Thinking tokens consumed by Adaptive Thinking */
    readonly thinkingTokens: number;
}

/**
 * Complete pipeline execution context for EMF metric emission.
 */
export interface PipelineMetricsContext {
    /** Runtime environment (e.g. 'development', 'production') */
    readonly environment: string;
    /** Foundation model identifier (e.g. 'eu.anthropic.claude-sonnet-4-6') */
    readonly model: string;
    /** Article slug (high-cardinality — logged but not a dimension) */
    readonly slug: string;
    /** Pipeline mode: 'kb-augmented' or 'legacy-transform' */
    readonly mode: 'kb-augmented' | 'legacy-transform';
    /** Token usage from Bedrock response */
    readonly tokenUsage: TokenUsage;
    /** Stage timing measurements in milliseconds */
    readonly stageDurations: StageDurations;
    /** Complexity tier classification */
    readonly complexityTier: ComplexityTier;
    /** AI-scored technical confidence (0–100) */
    readonly technicalConfidence: number;
    /** Input draft size in bytes */
    readonly draftSizeBytes: number;
    /** QA validation score (0-100), if QA agent was run */
    readonly qaScore?: number;
    /** QA agent processing duration in ms, if applicable */
    readonly qaDurationMs?: number;
}

/**
 * Per-stage duration measurements in milliseconds.
 */
export interface StageDurations {
    /** Total end-to-end pipeline duration */
    readonly totalMs: number;
    /** Time spent reading the draft from S3 */
    readonly readDraftMs?: number;
    /** Time spent on KB retrieval (KB-augmented mode only) */
    readonly kbRetrievalMs?: number;
    /** Time spent calling the Bedrock Converse API */
    readonly bedrockConverseMs?: number;
    /** Time spent parsing the Bedrock response */
    readonly parseResponseMs?: number;
    /** Time spent on QA validation (2-agent PoC) */
    readonly qaValidationMs?: number;
    /** Time spent writing results to S3 */
    readonly s3WriteMs?: number;
    /** Time spent writing metadata to DynamoDB */
    readonly dynamoWriteMs?: number;
}

/**
 * Failure context for error-path metric emission.
 */
export interface FailureMetricsContext {
    /** Runtime environment */
    readonly environment: string;
    /** Foundation model identifier */
    readonly model: string;
    /** Pipeline stage where the failure occurred */
    readonly failureStage: FailureStage;
    /** Article slug, if known at time of failure */
    readonly slug?: string;
    /** Error message (truncated for safety) */
    readonly errorMessage: string;
    /** Elapsed milliseconds before failure */
    readonly elapsedMs: number;
}

