// STAGE TIMER
// =============================================================================

/**
 * High-resolution pipeline stage timer.
 *
 * Records start/end timestamps for each named pipeline stage and
 * produces a {@link StageDurations} object for EMF emission.
 *
 * @example
 * ```typescript
 * const timer = new StageTimer();
 * timer.start('readDraft');
 * const draft = await readDraftFromS3(bucket, key);
 * timer.end('readDraft');
 *
 * timer.start('bedrockConverse');
 * const result = await transformWithBedrock(msg, complexity);
 * timer.end('bedrockConverse');
 *
 * const durations = timer.getDurations();
 * // { totalMs: 12500, readDraftMs: 250, bedrockConverseMs: 12250 }
 * ```
 */
export class StageTimer {
    /** Pipeline-level start timestamp */
    private readonly pipelineStart: number;

    /** Per-stage start timestamps */
    private readonly starts = new Map<string, number>();

    /** Per-stage elapsed milliseconds */
    private readonly durations = new Map<string, number>();

    constructor() {
        this.pipelineStart = Date.now();
    }

    /**
     * Mark the start of a pipeline stage.
     *
     * @param stage - Stage name matching a key in {@link StageDurations}
     */
    public start(stage: string): void {
        this.starts.set(stage, Date.now());
    }

    /**
     * Mark the end of a pipeline stage and record its duration.
     *
     * @param stage - Stage name (must match a previous `start()` call)
     * @throws Error if `start()` was not called for this stage
     */
    public end(stage: string): void {
        const startTime = this.starts.get(stage);
        if (startTime === undefined) {
            throw new Error(`StageTimer: end('${stage}') called without matching start()`);
        }
        this.durations.set(stage, Date.now() - startTime);
    }

    /**
     * Get the total elapsed time since the timer was created.
     *
     * @returns Elapsed milliseconds
     */
    public elapsed(): number {
        return Date.now() - this.pipelineStart;
    }

    /**
     * Build a {@link StageDurations} object from all recorded stages.
     *
     * @returns Stage durations with `totalMs` always populated
     */
    public getDurations(): StageDurations {
        return {
            totalMs: this.elapsed(),
            readDraftMs: this.durations.get('readDraft'),
            kbRetrievalMs: this.durations.get('kbRetrieval'),
            bedrockConverseMs: this.durations.get('bedrockConverse'),
            parseResponseMs: this.durations.get('parseResponse'),
            qaValidationMs: this.durations.get('qaValidation'),
            s3WriteMs: this.durations.get('s3Write'),
            dynamoWriteMs: this.durations.get('dynamoWrite'),
        };
    }
}

